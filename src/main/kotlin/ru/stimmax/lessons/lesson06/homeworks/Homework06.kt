//Создайте функцию, которая конвертирует температуру из градусов Цельсия в Фаренгейты
// и наоборот в зависимости от указанной единицы измерения (C/F). Единицу измерения
// нужно передать вторым аргументом функции. Несколько аргументов передаются через запятую.
// Распечатай в консоль результат конвертации с добавлением единицы измерения.
// Чтобы добавить единицу измерения после результата используй функцию печати без
// переноса строки print("C") или print("F").

fun task6 (value: Double, unit: String) {
    if (unit.equals("C", ignoreCase = true)) {
        val f = value * 9 / 5 + 32
        print(f)
        print("F")
    } else if (unit.equals("F", ignoreCase = true)) {
        val c = (value - 32) * 5 / 9
        print(c)
        print("C")
    } else {
        print("Некорректная единица")
    }
    println()
}


//Задание 7: "Подбор одежды по погоде"

//Напишите функцию, которая на основе температуры воздуха рекомендует тип одежды:
// "куртка и шапка" при температуре ниже +10, "ветровка" от +10 до +18 градусов включительно
// и "футболка и шорты" при температуре выше +18 градусов. При температурах ниже -30 и выше +35 рекомендуйте не выходить из дома.

fun task7(temperature: Int) {
    if (temperature < -30 || temperature > 35) {
        println("Не выходить из дома")
    } else if (temperature < 10) {
        println("Куртка и шапка")
    } else if (temperature in 10..18) {
        println("Ветровка")
    } else {
        println("Футболка и шорты")
    }
}


//Задание 8: "Выбор фильма по возрасту"

//Кинотеатр предлагает фильмы разных возрастных категорий. Напишите функцию, которая
// принимает возраст зрителя и возвращает доступные для него категории фильмов:
// "детские" (от 0 до 9), "подростковые" (от 10 до 18), "18+" для остальных.

fun task8(age: Int) {
    if (age in 0..9) {
        println("детские")
    } else if (age in 10..18) {
        println("подростковые")
    } else if (age > 18) {
        println("18+")
    } else {
        println("недопустимый возраст")
    }
}
