//Задание 4: "Расчет бонусных баллов"

//Клиенты интернет-магазина получают бонусные баллы за покупки. Напишите функцию,
// которая принимает сумму покупки и печатает в консоль количество бонусных баллов:
// 2 балла за каждые 100 рублей при сумме покупки до 1000 рублей и 3 балла за каждые 100 рублей при сумме свыше этого.

fun task4(purchaseAmount: Int) {
    val hundreds = purchaseAmount / 100  // целочисленное количество сотен рублей
    val bonus = if (purchaseAmount <= 1000) {
        hundreds * 2
    } else {
        hundreds * 3
    }
    println("Бонусные баллы: $bonus")
}

//Задание 5: "Определение типа документа"

//В системе хранения документов каждый файл имеет расширение. Напишите функцию,
// которая на основе расширения файла печатает в консоль его тип:
// "Текстовый документ", "Изображение", "Таблица" или "Неизвестный тип".

fun taskFile(extension: String) {
    if (extension.equals("txt", ignoreCase = true)) {
        print("Текстовый документ")
    } else if (extension.equals("jpg", ignoreCase = true) || extension.equals("png", ignoreCase = true)) {
        print("Изображение")
    } else if (extension.equals("xls", ignoreCase = true) || extension.equals("xlsx", ignoreCase = true)) {
        print("Таблица")
    } else {
        print("Неизвестный тип")
    }
    println()
}


